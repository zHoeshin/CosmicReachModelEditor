function prettifyJsonWithExceptions(obj, indent = '\t') {
    let jsonStr = '{\n';
    const exceptions = ['arraysOfNumbers', 'objectsWithUvProperty']; // Placeholder for your conditions

    function addExceptionKey(key, value, indent) {
        if (Array.isArray(value) && value.every(Number.isFinite)) {
            jsonStr += `${indent}"${key}": ${JSON.stringify(value)},\n`;
        }
        else if (typeof value === 'object' && value!== null && value.uv !== undefined) {
            jsonStr += `${indent}"${key}": ${JSON.stringify(value)},\n`;
        }else{
            jsonStr += `${indent}"${key}": ${JSON.stringify(value, undefined, "\t")},\n`
        }
    }

    function traverseAndBuild(obj, indent) {
        Object.keys(obj).forEach(key => {
            addExceptionKey(key, obj[key], indent);
            if (typeof obj[key] === 'object' && obj[key]!== null) {
                traverseAndBuild(obj[key], indent + "\t");
            }
        });
    }

    traverseAndBuild(obj, indent);

    jsonStr += '}';
    return jsonStr;
}
